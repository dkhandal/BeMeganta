package com.sarvika.bmegantanative.model;

public class Labels {

   private String listPrice;
   private String noPrice;
   private String youSave;
   private String savePlus;
   private String points;
   private String callprice;
   private String quickinfo;
   private String itemCode;

   public String getListPrice() {
       return listPrice;
   }

   public void setListPrice(String listPrice) {
       this.listPrice = listPrice;
   }

   public String getNoPrice() {
       return noPrice;
   }

   public void setNoPrice(String noPrice) {
       this.noPrice = noPrice;
   }

   public String getYouSave() {
       return youSave;
   }

   public void setYouSave(String youSave) {
       this.youSave = youSave;
   }

   public String getSavePlus() {
       return savePlus;
   }

   public void setSavePlus(String savePlus) {
       this.savePlus = savePlus;
   }

   public String getPoints() {
       return points;
   }

   public void setPoints(String points) {
       this.points = points;
   }

   public String getCallprice() {
       return callprice;
   }

   public void setCallprice(String callprice) {
       this.callprice = callprice;
   }

   public String getQuickinfo() {
       return quickinfo;
   }

   public void setQuickinfo(String quickinfo) {
       this.quickinfo = quickinfo;
   }

   public String getItemCode() {
       return itemCode;
   }

   public void setItemCode(String itemCode) {
       this.itemCode = itemCode;
   }

}
